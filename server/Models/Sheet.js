const mongoose = require('mongoose');

const sheetSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    unique: true
  },
  description: {
    type: String,
    required: true
  },
  createdAt: {
    type: Date,
    default: Date.now
  },
  updatedAt: {
    type: Date,
    default: Date.now
  }
});

const Sheet = mongoose.model('Sheet', sheetSchema);

module.exports = Sheet;
